fn pos<N: PartialOrd>(start: N, end: N, num: N) -> Ordering {
    if num >= start && num <= end {
        Ordering::Equal
    }
    else if num > start {
        Ordering::Less
    }
    else if num < end {
        Ordering::Greater
    }
    else {
        unreachable!()
    }
}

